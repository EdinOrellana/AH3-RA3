   Controlador control=new Controlador();
void Editar() {
        int fila = Usuarios.Tabla.getSelectedRow();
        if (fila < -1) {
            JOptionPane.showMessageDialog(null, "Seleccione una Fila");
        } else {
            ArrayList<Modelo> Datos = control.obtener(cn,Tabla.getValueAt(fila, 1).toString());
            if (Datos.size() == 0) {
                JOptionPane.showMessageDialog(null, "Usuario No Encontrado");
            } else {
        usuario_id=Datos.get(0).getUsuario_id();
        Guardar_Editar_Borrar.jtflContra.setText(Datos.get(0).getContrasenia());
        Guardar_Editar_Borrar.jtflCorreo.setText(Datos.get(0).getCorreo_electronico());
        Guardar_Editar_Borrar.jtflDireccion.setText(Datos.get(0).getDireccion());
        Guardar_Editar_Borrar.jtflFechaNacimiento.setDate(Datos.get(0).getFecha_nacimiento());//parentesis de mas
        Guardar_Editar_Borrar.jtflTelefono.setText(String.valueOf(Datos.get(0).getTelefono()));
        Guardar_Editar_Borrar.jtflapellido.setText(Datos.get(0).getApellido());
        Guardar_Editar_Borrar.jtflnombre.setText(Datos.get(0).getNombre());
            }
        }
    }

